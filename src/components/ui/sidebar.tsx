const SidebarMenuButton = React.forwardRef<
  HTMLAnchorElement,
  React.ComponentProps<typeof Link> & {
    isActive?: boolean
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      isActive = false,
      variant = "default",
      size = "default",
      tooltip,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, state } = useSidebar()

    const buttonContent = (
      <>
        {React.Children.map(children, (child, index) => {
          if (React.isValidElement(child) && index === 0) { // Icon
            return React.cloneElement(child, {
              className: cn(child.props.className),
            } as React.HTMLAttributes<HTMLElement>);
          }
          if (typeof child === 'string' || (React.isValidElement(child) && child.type === 'span')) { // Text
            return <span className={cn('flex-1 truncate', state === 'collapsed' && 'hidden')}>{child}</span>;
          }
          return child;
        })}
      </>
    );

    const button = (
      <Link
        ref={ref}
        data-sidebar="menu-button"
        data-size={size}
        data-active={isActive}
        className={cn(sidebarMenuButtonVariants({ variant, size }), state === 'collapsed' && "justify-center", className)}
        {...props}
      >
        {buttonContent}
      </Link>
    )

    if (!tooltip) {
      return button
    }

    if (typeof tooltip === "string") {
      tooltip = {
        children: tooltip,
      }
    }

    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          hidden={state !== "collapsed" || isMobile}
          {...tooltip}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

